print ("メタンの流量を入力してください（0〜50）")

a = input()     #メタンの流量入力
a = float(a)    #文字列を数値に変換

b = 50 - a      #ヘキサフルオロエタンの流量計算

c = 1.40    #Arのコンバージョンファクター値
d = 0.75    #メタンのコンバージョンファクター値
e = 0.25    #ヘキサフルオロエタンのコンバージョンファクター値

f = a * c / d   #メタンの設定値計算
g = b * c / e   #ヘキサフルオロエタンの設定値計算

print("メタンの設定値は" + str(f))
print("ヘキサフルオロエタンの設定値は" + str(g))
